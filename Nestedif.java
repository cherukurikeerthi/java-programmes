public class Nestedif	
{	
     public static void main(String[] args)	
	 {
              //nested if	
              int y=16;	
              int z=15;	
              if (z>y){	
                System.out.println("z is greater than y");	
              }					
              if (z<y){	
     	        System.out.println("y is greater than z");	
			  }		
     }	
}	 